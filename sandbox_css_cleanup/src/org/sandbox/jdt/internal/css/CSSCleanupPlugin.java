package org.sandbox.jdt.internal.css;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle.
 */
public class CSSCleanupPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "sandbox_css_cleanup"; //$NON-NLS-1$

	private static CSSCleanupPlugin plugin;

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	public static CSSCleanupPlugin getDefault() {
		return plugin;
	}
}
